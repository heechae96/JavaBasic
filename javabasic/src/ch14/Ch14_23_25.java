		// toArray
		//	-> 스트림의 모든 요소를 배열로 반환
		// ★reduce★
		//	-> 스트림의 요소를 하나씩 줄여가면서 계산한다
		// ★collect★
		//	-> 스트림의 요소를 수집한다
		//	-> 주로 요소를 그룹화하거나 분할한 결과를 컬렉션에 담아 반환하는데 사용된다
		
